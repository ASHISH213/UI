
var a = 6;
var b = 1;

// AND Operation
console.log("A & B = " + (a & b));

// OR operation
console.log("A | B = " + (a | b));

// xOR operation
console.log("A ^ B = " + (a ^ b));


// NOT operation
console.log("~A = " + (~a));

// Sign Propagating Right Shift
console.log("A >> B = " + (a >> b));

// Zero Fill Right Shift
console.log("A >>> B = " + (a >>> b));

// Left Shift
console.log("A << B = " + (a << b));
